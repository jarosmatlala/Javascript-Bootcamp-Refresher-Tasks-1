//EXCERCISE 2

let number1 = 20;
let number2 = 25;

// Greater than
let GreaterThan = number1 > number2;
console.log("Is number1 greater than number2?", GreaterThan); 

// Less than
let LessThan = number1 < number2;
console.log("Is number1 less than number2?", LessThan); 

// Greater than or equal to
let GreaterThanOrEqualTo = number1 >= number2;
console.log("Is number1 greater than or equal to number2?", GreaterThanOrEqualTo); 

let LessThanOrEqualTo = number1 <= number2;
console.log("Is number1 less than or equal to number2?", LessThanOrEqualTo); 

// Equal to
let EqualTo = number1 == number2;
console.log("Is number1 equal to number2?", EqualTo); 
// Not equal to
let NotEqualTo = number1 != number2;
console.log("Is number1 not equal to number2?", NotEqualTo); 

// Strictly equal to 
let StrictlyEqualTo = number1 === number2;
console.log("Is number1 strictly equal to number2?", StrictlyEqualTo); 

// Strictly not equal to 
let StrictlyNotEqualTo = number1 !== number2;
console.log("Is number1 strictly not equal to number2?", StrictlyNotEqualTo);


